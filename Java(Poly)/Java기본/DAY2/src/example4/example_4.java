package example4;

public class example_4 {

	public static void main(String[] args) {
		int a = 5;
		int b= 3;
		System.out.println(a + b);
		System.out.println(a - b);
		System.out.println(a * b);
		System.out.println(a / b);
		System.out.println(a % b);
		System.out.println(a++);
		System.out.println(b--);
		
		a= 35;
		b= 30;
		System.out.println(a);
		System.out.println(a++);
		System.out.println(a);
		System.out.println(--a);
	}

}
