package Example07;

public class k05_Cal {
	
	public static int k05_sum(int k05_a, int k05_b, int k05_c) {
		return k05_a + k05_b + k05_c;
	} // 3개의 변수를받아 합 return
	
	public static int k05_sum(int k05_a, int k05_b, int k05_c, int k05_d) {
		return k05_a + k05_b + k05_c + k05_d;
	} // 4개의 변수를받아 합 return
	
	public static int k05_sum(int k05_a, int k05_b, int k05_c, int k05_d, int k05_e) {
		return k05_a + k05_b + k05_c + k05_d + k05_e;
	} // 5개의 변수를받아 합 return
	
	public static double k05_ave(int k05_a, int k05_b, int k05_c) {
		return (k05_a + k05_b + k05_c) / 3;
	} // 3개의 변수를받아 평균 return
	
	public static double k05_ave(int k05_a, int k05_b, int k05_c, int k05_d) {
		return (k05_a + k05_b + k05_c + k05_d) / 4;
	} // 4개의 변수를받아 평균 return
	
	public static double k05_ave(int k05_a, int k05_b, int k05_c, int k05_d, int k05_e) {
		return (k05_a + k05_b + k05_c + k05_d + k05_e) / 5;
	} // 5개의 변수를받아 평균 return

}
